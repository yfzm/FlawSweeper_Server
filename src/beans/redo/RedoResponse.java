package beans.redo;

public class RedoResponse {
    private Boolean status;
    private int redoId;

    public Boolean getStatus() {
        return status;
    }

    public void setStatus(Boolean status) {
        this.status = status;
    }

    public int getRedoId() {
        return redoId;
    }

    public void setRedoId(int redoId) {
        this.redoId = redoId;
    }
}
